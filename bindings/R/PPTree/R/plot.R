# ======================================================================
# PPTree Visualization
#
# Native ggplot2 rendering for PPTree and PPForest models.  Provides:
#   - Tree structure diagram with embedded class histograms at each node
#   - Variable importance bar chart (projection, weighted, permuted)
#   - Projection histogram at any split node
#   - Decision boundary plots (1D, 2D, and pairwise for p > 2)
#   - Mosaic layout combining structure + importance + boundaries
#
# All plots use a shared design system (constants below) for cohesive
# styling.  Data-mapped colors (class fill/colour) use ggplot2 scales
# so users can override them with + scale_fill_manual(...).
#
# Architecture:
#   - C++ (Visualization.hpp/cpp) computes geometry: node data routing,
#     boundary line clipping, region polygon clipping (Sutherland-Hodgman),
#     and tree layout positioning.
#   - R (this file) handles rendering: translates C++ output into ggplot2
#     layers and assembles composite layouts via the grid package.
# ======================================================================

