#' Guard: abort with a helpful message if ggplot2 is not installed.
#'
#' Called at the top of every public plot method (plot.PPTree, plot.PPForest).
#' @noRd
check_ggplot2 <- function() {
  if (!requireNamespace("ggplot2", quietly = TRUE)) {
    stop(
      "Package 'ggplot2' is required for plot methods. ",
      "Install it with install.packages('ggplot2').",
      call. = FALSE
    )
  }
}

# --- Design system ---
#
# Named constants for line weights, point sizes, alpha values, and
# structural colors used across all plot types.  These ensure a cohesive
# look and make it easy to adjust the overall visual style in one place.
#
# The design system has two layers:
#
#   1. Structural constants (below): fixed values for non-data elements
#      like edges, borders, thresholds, and decision boundaries.  These
#      use neutral greys to avoid competing with data-mapped colors.
#
#   2. Data-mapped colors: per-class fill and colour generated by
#      get_class_colors() using evenly-spaced HCL hues.  Applied via
#      scale_fill_manual / scale_color_manual so users can override
#      with + scale_fill_manual(values = ...).
#
# Node dimensions (pptree_node_w, etc.) must match the C++ LayoutParams
# defaults so that histogram bars and leaf labels align with the
# rectangles produced by compute_tree_layout().

# Line weights
pptree_lw_light  <- 0.4    # edges, node borders
pptree_lw_medium <- 0.6    # boundary lines, thresholds

# Point sizes
pptree_pt_small  <- 1.2    # pairwise facets
pptree_pt_medium <- 1.8    # 2D boundary

# Alpha values
pptree_alpha_region <- 0.25  # decision region fill
pptree_alpha_hist   <- 0.65  # histogram bars
pptree_alpha_leaf   <- 0.30  # leaf fill
pptree_alpha_proj   <- 0.60  # projection histogram

# Structural colors (not data-mapped)
pptree_col_edge      <- "grey55"
pptree_col_border    <- "grey50"
pptree_col_threshold <- "grey25"
pptree_col_boundary  <- "grey30"
pptree_col_tick      <- "grey40"
pptree_col_bar       <- "#5B8BA0"

# Node dimensions (must match C++ LayoutParams)
pptree_node_w <- 0.8; pptree_node_h <- 0.7
pptree_leaf_w <- 0.5; pptree_leaf_h <- 0.3

#' Shared ggplot2 theme for all non-structure plots.
#'
#' Uses \code{theme_minimal} as a base with slightly larger titles and no
#' minor gridlines.  The tree structure plot uses \code{theme_void()}
#' instead (no axes, no grid) since it renders on an abstract 2D canvas.
#' @noRd
pptree_theme <- function() {
  ggplot2::theme_minimal(base_size = 11) +
  ggplot2::theme(
    plot.title       = ggplot2::element_text(size = ggplot2::rel(1.1)),
    panel.grid.minor = ggplot2::element_blank()
  )
}

